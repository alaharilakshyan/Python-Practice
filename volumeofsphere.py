r = int(input("Enter the radius: "))
pi = 3.14
volume = (4/3) * pi * (r**3)
print("The volume of the sphere is: ", volume)
#formula for volume of sphere is (4/3) * pi * r^3 where r is the radius of the sphere and pi is a constant value which is 3.14.
#used the input function to take the radius as input and then print it in the required format.
#used the print function to print the final result.
#used the ** operator to raise the radius to the power of 3 and then used the * operator to multiply the values and get the final result.