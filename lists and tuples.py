#lists and tuples
anime = ['onepiece', 'bleach', 'Naruto', 'dragonball']
manga = ('demonslayer', 'sakomoto days', 'Boruto TBV', 'Tokyo Revengers')
print(anime)
print(manga)
#lists are mutable and tuples are immutable.
#lists are defined by [] and tuples are defined by ()
#lists can be changed and tuples cannot be changed.
#lists can be used to store multiple values of different data types.
#tuples can be used to store multiple values of different data types.
#lists can be used to store multiple values of same data types.
#tuples can be used to store multiple values of same data types.
